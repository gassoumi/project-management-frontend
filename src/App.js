const store = configureStore();


moment.locale('fr');
// cleat token when it became invalid
const clearAuthentication = (messageKey) => (dispatch, getState) => {
  //dispatch(displayAuthError(messageKey));
  //console.log("clearAuthentication is called ",messageKey);
  return dispatch({
    type: LOGOUT_SUCCESS,
  });
};

const actions = bindActionCreators({clearAuthentication}, store.dispatch);

setupAxios(store.dispatch,
  () => actions.clearAuthentication('login.error.unauthorized'));

class App extends Component {

  componentDidMount() {
    store.dispatch(loadUser());
  }

  render() {
    return (
      <div style={{
        wordWrap: 'break-word',
        wordBreak: 'break-word',
      }}>
        <Provider store={store}>
          <Alerts/>
          <ToastContainer limit={3} autoClose={2000}/>
          <BrowserRouter>
            <CssBaseline/>
            <ScrollToTop>
              <Routes/>
            </ScrollToTop>
          </BrowserRouter>
        </Provider>
      </div>
    );
  }
}

export default App;
